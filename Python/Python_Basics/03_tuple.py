my_tuple  = (1,2,3,4)

print(my_tuple)

# Tuple methods
print(len(my_tuple))
print(my_tuple.count(3)) # this will return the count of the element 3 in the tuple


# Accessing tuple
print(my_tuple[1]) # this will print the element 2 as the indexing is 0 based.

# Tuples are unchangable i.e once created we cannot add, update or remove elements in the tuple